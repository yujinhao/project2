package cn.edu.lingnan.dto;

public class ManagersDTO {	
	private String ManagerId;		
	private String  ManagerName;
	private String ManagerSex;
	private int ManagerTel;
	private String ManagerAddr;
	private String ManagerPassw;
	public String getManagerId() {
		return ManagerId;
	}
	public void setManagerId(String managerId) {
		ManagerId = managerId;
	}
	public String getManagerName() {
		return ManagerName;
	}
	public void setManagerName(String managerName) {
		ManagerName = managerName;
	}
	public String getManagerSex() {
		return ManagerSex;
	}
	public void setManagerSex(String managerSex) {
		ManagerSex = managerSex;
	}
	public int getManagerTel() {
		return ManagerTel;
	}
	public void setManagerTel(int managerTel) {
		ManagerTel = managerTel;
	}
	public String getManagerAddr() {
		return ManagerAddr;
	}
	public void setManagerAddr(String managerAddr) {
		ManagerAddr = managerAddr;
	}
	public String getManagerPassw() {
		return ManagerPassw;
	}
	public void setManagerPassw(String managerPassw) {
		ManagerPassw = managerPassw;
	}

}